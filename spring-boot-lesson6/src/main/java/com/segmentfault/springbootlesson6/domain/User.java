package com.segmentfault.springbootlesson6.domain;

/**
 * @author 夸克
 * @date 2018/11/11 12:18
 */
public class User {
    private int age;
    private int id;
    private String name;

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
